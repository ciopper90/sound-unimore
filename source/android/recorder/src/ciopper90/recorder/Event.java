package ciopper90.recorder;

public class Event {
	private String evento;
	private String tempo;
		
	public Event(String evento, String tempo) {
		super();
		this.evento = evento;
		this.tempo = tempo;
	}
	public String getEvento() {
		return evento;
	}
	public String getTempo() {
		return tempo;
	}
	
	

}
